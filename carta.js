(() => {
  // ===========================================================
  // 🎛️ FILTRAR CATEGORÍAS
  // ===========================================================
  function activarFiltros() {
    const botones = document.querySelectorAll(".categoria-btn");

    botones.forEach((btn) => {
      btn.addEventListener("click", () => {
        botones.forEach((b) => b.classList.remove("active"));
        btn.classList.add("active");

        const categoria = btn.dataset.categoria;
        const bloques = document.querySelectorAll(".carta-contenido .bloque");

        bloques.forEach((bloque) => {
          const bloqueCat = bloque.dataset.categoria;

          if (categoria === "todo" || categoria === bloqueCat) {
            bloque.style.display = "block";
          } else if (bloque.classList.contains("leyenda-alergenos")) {
            bloque.style.display = "block";
          } else {
            bloque.style.display = "none";
          }
        });
      });
    });
  }

  activarFiltros();
})();
